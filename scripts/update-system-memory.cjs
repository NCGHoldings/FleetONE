#!/usr/bin/env node
/**
 * update-system-memory.cjs
 *
 * Called by the pre-push git hook.
 * Updates the "Last Updated" timestamp and appends a development log entry
 * to SYSTEM_MEMORY.local.md with the latest git commit info.
 *
 * This script IS committed to git.
 * SYSTEM_MEMORY.local.md is NOT committed (it's in .gitignore).
 *
 * Uses .cjs extension because the project root has "type": "module" in package.json.
 */

const fs   = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const MEMORY_FILE = path.join(__dirname, '..', 'SYSTEM_MEMORY.local.md');

// ── Helpers ───────────────────────────────────────────────────────────────────

function run(cmd) {
  try {
    return execSync(cmd, { encoding: 'utf8', stdio: ['pipe', 'pipe', 'pipe'] }).trim();
  } catch {
    return null;
  }
}

function today() {
  return new Date().toISOString().slice(0, 10); // YYYY-MM-DD
}

function nowISO() {
  return new Date().toISOString().replace('T', ' ').slice(0, 19) + ' UTC';
}

// ── Git info ──────────────────────────────────────────────────────────────────

const commitHash   = run('git rev-parse --short HEAD')      || 'unknown';
const commitMsg    = run('git log -1 --pretty=%s')           || 'unknown';
const branch       = run('git rev-parse --abbrev-ref HEAD')  || 'unknown';
const author       = run('git log -1 --pretty=%an')          || 'unknown';
const changedFiles = run('git diff --name-only HEAD~1 HEAD 2>/dev/null') || '';

// ── Read existing file ────────────────────────────────────────────────────────

if (!fs.existsSync(MEMORY_FILE)) {
  console.log('   ⚠️  SYSTEM_MEMORY.local.md not found — skipping update.');
  process.exit(0);
}

let content = fs.readFileSync(MEMORY_FILE, 'utf8');

// ── 1. Update "Last Updated" header line ─────────────────────────────────────

content = content.replace(
  /> Last Updated: .+/,
  `> Last Updated: ${today()} | Maintained by: pre-push hook (auto) + engineers (manual)`
);

// ── 2. Append to Development Log table (idempotent — skips if commit already logged) ──

const logEntry     = `| ${today()} | \`${commitHash}\` ${commitMsg} (branch: \`${branch}\`) | ${author} |`;
const logTableMarker = '| Date | Change | Author |';
const logSeparator   = '|------|--------|--------|';

if (content.includes(logTableMarker) && !content.includes(commitHash)) {
  const tableStart = content.indexOf(logTableMarker);
  const sepPos     = content.indexOf(logSeparator, tableStart);
  if (sepPos !== -1) {
    const insertAt = sepPos + logSeparator.length;
    content = content.slice(0, insertAt) + '\n' + logEntry + content.slice(insertAt);
  }
}

// ── 3. Upsert "Last Push Snapshot" section ───────────────────────────────────

const fileList = changedFiles
  ? changedFiles.split('\n').filter(Boolean).map(f => `  ${f}`).join('\n')
  : '  (no diff available — first commit or squash)';

const pushSection =
`## 🚀 LAST PUSH SNAPSHOT
> Auto-generated by pre-push hook — do not edit manually.

| Field | Value |
|-------|-------|
| **Date** | ${nowISO()} |
| **Branch** | \`${branch}\` |
| **Commit** | \`${commitHash}\` |
| **Message** | ${commitMsg} |
| **Author** | ${author} |
| **Files Changed** | ${changedFiles ? changedFiles.split('\n').filter(Boolean).length + ' files' : '—'} |

### Changed Files
\`\`\`
${fileList}
\`\`\`
`;

const footerMarker = '*This file is auto-updated';

if (content.includes('## 🚀 LAST PUSH SNAPSHOT')) {
  // Replace the existing section up to the next ## heading or the footer
  content = content.replace(
    /## 🚀 LAST PUSH SNAPSHOT[\s\S]*?(?=\n## |\n\*This file)/,
    pushSection.trimEnd() + '\n\n'
  );
} else {
  // Insert before the footer line
  const footerPos = content.lastIndexOf(footerMarker);
  if (footerPos !== -1) {
    content =
      content.slice(0, footerPos) +
      '\n---\n\n' + pushSection + '\n' +
      content.slice(footerPos);
  } else {
    content += '\n\n---\n\n' + pushSection;
  }
}

// ── Write back ────────────────────────────────────────────────────────────────

fs.writeFileSync(MEMORY_FILE, content, 'utf8');
console.log(`   ✅ SYSTEM_MEMORY.local.md updated — commit ${commitHash} on branch '${branch}'`);
